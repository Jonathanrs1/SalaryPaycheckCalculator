package br.edu.univas.si.lab6.calc.to;

public class ValoresTO {

	private float inss;
	private float irpf;
	private float liquidSalary;
	
	
	
	public float getInss() {
		return inss;
	}
	public void setInss(float inss) {
		this.inss = inss;
	}
	public float getIrpf() {
		return irpf;
	}
	public void setIrpf(float irpf) {
		this.irpf = irpf;
	}
	public float getLiquidSalary() {
		return liquidSalary;
	}
	public void setLiquidSalary(float liquidSalary) {
		this.liquidSalary = liquidSalary;
	}
	
}
